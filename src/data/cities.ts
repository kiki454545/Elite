export function searchCities(countryCode: string, query: string): string[] {
  // Get unique cities only (remove duplicates)
  const cities = [...new Set(CITIES_BY_COUNTRY[countryCode] || [])]

  if (!query || query.length < 2) {
    return cities.slice(0, 10) // Show first 10 cities if query is too short
  }

  // Normalize query: remove accents and replace spaces with hyphens for comparison
  const normalizedQuery = query
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, '-') // Replace spaces with hyphens

  return cities
    .filter(city => {
      // Normalize city name: remove accents
      const normalizedCity = city
        .toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')

      // Search only at the beginning of the city name
      return normalizedCity.startsWith(normalizedQuery)
    })
    .slice(0, 20) // Limit to 20 results
}
